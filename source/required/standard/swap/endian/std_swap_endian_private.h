    /**
     * @brief 字节序
     *
     */
    typedef enum endian_e
    {
        E_LIEELE_ENDIAN = FALSE, /**> 小端 */
        E_BIG_ENDIAN    = TRUE,  /**> 大端 */
    } endian_e;

    /*union*********************************************************************/

    /**
     * @brief u16类型数据
     *
     */
    typedef union data16_u
    {
        u16 u16_data;    /**> u16数据 */

        struct s_byte
        {
            u8 u8_byte0; /**> 第1字节 */
            u8 u8_byte1; /**> 第2字节 */
        } s_byte;
    } data16_u;

    /**
     * @brief u32类型数据
     *
     */
    typedef union data32_u
    {
        u32 u32_data;    /**> u32数据 */

        struct s_byte
        {
            u8 u8_byte0; /**> 第1字节 */
            u8 u8_byte1; /**> 第2字节 */
            u8 u8_byte2; /**> 第3字节 */
            u8 u8_byte3; /**> 第4字节 */
        } s_byte;
    } data32_u;

    /**
     * @brief u64类型数据
     *
     */
    typedef union data64_u
    {
        u64 u64_data;    /**> u64数据 */

        struct s_byte
        {
            u8 u8_byte0; /**> 第1字节 */
            u8 u8_byte1; /**> 第2字节 */
            u8 u8_byte2; /**> 第3字节 */
            u8 u8_byte3; /**> 第4字节 */
            u8 u8_byte4; /**> 第5字节 */
            u8 u8_byte5; /**> 第6字节 */
            u8 u8_byte6; /**> 第7字节 */
            u8 u8_byte7; /**> 第8字节 */
        } s_byte;
    } data64_u;

